
public class ArrayLanguageTranslation {
	
		//Field
		private String englishWord;
		private String finnishWord;
		
		//Constructor
		public ArrayLanguageTranslation(String englishWord, String finnishWord) {
			this.englishWord = englishWord;
			this.finnishWord = finnishWord;
		}
		//Methods
		public String getEnglishWord() {
			return englishWord;
		}
		public String getFinnishWord() {
			return finnishWord; 
		}

	}

